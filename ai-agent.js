// Merge AI translations with main translations
if (typeof translations !== 'undefined') {
    Object.keys(aiTranslations).forEach(lang => {
        translations[lang] = { ...translations[lang], ...aiTranslations[lang] };
    });
}


// Apply translations on page load
document.addEventListener('DOMContentLoaded', () => {
    const savedLang = localStorage.getItem('language') || 'ru';

    // Apply translations
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[savedLang][key]) {
            el.innerHTML = translations[savedLang][key];
        }
    });

    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        const key = el.getAttribute('data-i18n-placeholder');
        if (translations[savedLang][key]) {
            el.placeholder = translations[savedLang][key];
        }
    });

    // Set active language button
    document.querySelectorAll('.lang-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.textContent.toLowerCase() === savedLang) {
            btn.classList.add('active');
        }
    });
});

